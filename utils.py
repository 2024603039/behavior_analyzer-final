import streamlit as st
import json

# 🔹 퀴즈 데이터 로딩 (캐싱)
@st.cache_data
def load_questions():
    with open("data/questions.json", "r", encoding="utf-8") as f:
        return json.load(f)


# 🔹 결과 분석 (캐싱)
@st.cache_data
def analyze_result(score):

    if score["delay"] >= 5:
        return "지연 늪형"

    elif score["avoid"] >= 5:
        return "책임 회피형"

    elif score["escape"] >= 4:
        return "현실 도피형"

    elif score["plan"] >= 6:
        return "완벽주의 과부하형"

    elif score["direct"] >= 5:
        return "과몰입 돌진형"

    else:
        return "균형 성장형"


